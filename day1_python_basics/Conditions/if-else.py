# check the given age minor or major
Age = 20
if Age >= 18:
    print("Major.")
else :
    print("Minor.")

# check the given number is even or odd
Num = int(input("Number: "))
if Num % 2 ==0 :
    print("Even")
else :
    print("Odd")

# check the given string is present in the list or not
List = ["Python", "Java", "C++", "JavaScript"]
String = input("Enter a string: ")
if String in List:
    print("Present")
else :
    print("Not Present")
